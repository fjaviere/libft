#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p1;
	int		i;

	p1 = (char *)s;
	i = 0;
	while (*p1 != '\0')
	{
		i++;
		p1++;
	}
	while (i >= 0)
	{
		if (*p1 == (char)c)
		{
			return (p1);
		}
		p1--;
		i--;
	}
	return (0);
}
